public class Student {
    private String name;
    private String age;
    private String roll;

    public String getAge() {
        return age;
    }

    public String getName() {
        return name;
    }

    public String getRoll() {
        return roll;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setRoll(String roll) {
        this.roll = roll;
    }
}
